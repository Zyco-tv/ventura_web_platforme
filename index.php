       <h6 class="bottom__line"> <strong>des interdits en roleplay</strong> </h6> 
       <p><strong>1.0</strong> No-Pain: Le No-Pain est le fait de ne pas jouer la douleur.</p>
       <p>1.1 Métagaming: Le metagaming est le fait de prendre des informations HRP (discord, twitch, youtube, etc) afin de les utiliser en RP.</p>
       <p>1.2 Power Gaming: Le powergaming est le fait de réaliser des actions qui sont irréalisables dans la vraie vie grâce au moteur du jeu.
        <br>Exemple : je saute d’un pont et me relève comme si de rien n’était car je ne suis pas tombé dans le coma.</p>
       <p>1.3 Streamstalk/streamhack: Le streamstalk/streamhack est le fait de récolter des informations sur un stream.
        <br>Exemple : j’ai vu qu’il habite à tel endroit, je vais là-bas pour le rencontrer.</p>
       <p>1.4 No-Fear: Le No fear est le fait de ne pas être sensible à la peur quand la situation l'impose.
        <br>Exemple : Je me fais braquer par un groupe de 4 personnes armées alors que je fais le plein de mon véhicule et je continue comme si de rien n'était.</p>
       <p>1.5 Freekill: Le freekill est le fait de tuer quelqu'un pour des raisons non valables ou pour aucune raison.
        <br>Exemple : Je vois quelqu'un sortir du concessionnaire avec une voiture neuve, je le suis, dès qu'il s'arrête dans un endroit tranquille je le tue et prend sa voiture.</p>
       <p>1.6 Carkill: Le carkill est le fait d'écraser volontairement quelqu'un sans raison valable.</p>
       <p>1.7 Force-RP: Le force RP est le fait de forcer un joueur à jouer une scène/rôle sans lui laisser le choix, une échappatoire, quand bien même cela le dérange HRP.</p>
       <p>1.8 Drive-By: Le Drive By est le fait de tirer à partir d’un véhicule roulant, cette règle s'applique pour le conducteur uniquement. Les passagers pourront faire l’usage de leurs armes.</p>
       <p>1.9 Play to win: Vouloir gagner à tous les coups / Ne pas accepter la défaite.</p>
       <h6 class="bottom__line"><strong>Règlement hors-roleplay</strong></h6> 
       <p>2.1 Il est interdit d’utiliser des moyens de communication hors jeu (Discord, Skype, etc) sauf autorisation exceptionnelle du staff.</p>
       <p>2.2 Le discord est complètement HRP sauf la catégorie “SALON RP”.</p>
       <p>2.3 Il est interdit de spam un membre du staff pour n’importe quelle raison.</p>
       <p>2.4 Il est interdit d’avoir des propos discriminatoires, racistes, homophobes, politiques, sexistes.</p>
       <p>2.5 Ventura n'est pas un site de rencontre (éviter tout type d'harcèlement).</p>
       <p>2.6 Seul un membre du staff peut prendre la décision de bannir quelqu'un.</p>
       <p>2.7 Il est interdit de mp un développeur.</p>
       <p>2.8 L’utilisation de usebug est strictement interdite sous peine de bannissement, envisagez plutôt de faire remonter celui-ci au staff sur le site web "support". Qui sait vous pourriez toucher une récompense</p>
       <p>2.9 Pour une demande de remboursement veuillez vous référer au site web "support".</p>
       <p>2.1.1 Si un reboot se fait pendant votre scène, celle-ci doit continuer obligatoirement à la reconnexion lorsque les protagonistes sont de nouveau réunis.</p>
       <p>2.1.2 Si une personne manque au règlement ou bien ne joue pas rôleplay/fairplay veuillez continuez la scène le plus proprement possible et prévenez le staff.</p>
       <p>2.1.3 Il est interdit d'utiliser des logiciels autres que Alt:V pour jouer.</p>
       <p>2.1.4 Tout problème informatique ne sera la responsabilité de Ventura.</p>
       <p>2.1.5 Le partage de compte n'est pas autorisé.</p>
       <p>2.1.6 Le règlement peut être modifier à tout moment (nous vous informerons si c'est le cas).</p>
       <p>2.1.7 Interdiction de faire de la publicité sur un channel discord/ts de Ventura.</p>
       <h6 class="bottom__line"><strong>Règlement Rôleplay</strong></h6>
       <p>3.1 Il n'est pas possible de reconnaître la voix de quelqu'un qui porte un masque à vous de jouer en conséquence.</p>
       <p>3.2 Vous devez avoir un Nom RP sérieux, pas de nom comportant un jeu de mot ou nom de célébrité</p>
       <p>3.3 Le HRP vocal est strictement interdit dès lors que vous êtes connecté au serveur.
    </p>
       <p>3.4 Interdiction de quitter le jeu (ALT+F4) en pleine action RP, si vous êtes victime d’un crash reconnectez vous au plus vite sinon prévenez un staff si la scène était importante (victime, course poursuite, etc). Si vous devez vous déconnecter pour une bonne raison IRL, faites le comprendre de manière RP.
    </p>
       <p>3.5 Les transformateurs de voix sont autorisés à condition de voir avec un staff qui validera la qualité.
    </p>
       <p>3.6 Tout ce qui concerne les mutilations (couper la langue, oreilles, jambes, bras ou crever un oeil, etc..) doit rester raisonnable et réalisable. Exemple : on ne coupe pas les 2 bras, les 2 jambes, etc...
    </p>
       <p>3.7 Si votre véhicule est endommagé vous devez vous arrêter et appeler une entreprise de dépannage.
    </p>
       <p>3.8 Si vous êtes AFK et qu’il vous arrive quelque chose vous serez le seul responsable,ne restez pas AFK déconnectez vous.
    </p>
       <p>3.9 Il est interdit de faire des appels à des services publics/entreprises ayant pour but d’agresser ou de kidnapper une personne. Privilégier la recherche de son numéro ou bien quand il est hors service.
    </p>
       <p>3.1.1 Les scènes de viol ou harcèlement grave sont interdites.
    </p>
       <p>3.1.2 Il est interdit de kidnapper/coma une personne appartenant à un service comme LSPD, GDC, GOUV dans le but de récupérer leurs armes.
    </p>
       <p>3.1.3 Les crashs en hélicoptère ne donnent pas la mort RP mais il ne faut pas en abuser ! #LSPD
    </p>
       <p>3.1.4 Les zones chaudes sont contrôlées par des gangs, mafias, organisations, etc.. donc ce qui veut dire qu’il y a toujours quelqu’un (même si vous voyez personne). Si votre véhicule se retrouve dans un quartier en aucun cas vous pouvez allez le récupérer. Il est clairement déconseillé d'y rentrer sauf si vous voulez repartir en slip. Prenez des précautions !
    </p>
       <p>3.1.5 Ne jamais croire que vous êtes seul en ville, il y a 4 millions d’habitants donc agissez en conséquence.
    </p>
       <p>3.1.6 Si vous souhaitez faire une vidéo, photo, audio le téléphone doit être visible (première personne exigée). La 3ème personne n’est pas recevable.
    </p>
       <p>3.1.7 Les taupes dans les groupes ne sont pas autorisées (sauf dans certains cas veuillez vous référer au staff pour une demande).
    </p>
       <p>3.1.8 Interdiction de braquer un joueur au ATM et de lui demander de vider son compte. (le fair play avant tout)
    </p>
       <p>3.1.9 Si un joueur vient à ne pas respecter les règles continuez la scène le plus proprement possible jusqu’au bout, on ne veut pas entendre des “t’as pas le droit”, "c'est pas RP" etc...
    </p>
       <p>3.2.1 On ne se couche pas sur un toit ou à couvert derrière un mur pour prendre des informations visuel !!! Ce que votre personnage ne voit pas vous ne pouvez le prendre en compte.
    </p>
       <p>3.2.2 On ne fait pas de raccourci comme par exemple : (La zentorno appartient a Dupont car y a que lui qui l'a en ville) N'oubliez pas qu'il y a plusieurs millions d'habitants, il n'est pas le seul fortuné.
    </p>
       <p>3.2.3 Le retour sur zone après un coma est strictement interdit.
    </p>
       <p>3.2.4 Le LSPD, LSMS, Gouvernement sont des "zones safes" donc interdiction de faire un assaut dans l'établissement ou de voler un de leurs véhicules devant.
    </p>
       <p>3.2.5 Chaque groupe créé doit faire un dossier auprès du staff afin d'être "officiel" car les places seront limitées.
    </p>
    <p>3.2.6 Une fois menotté vous ne pouvez rien faire à part courir. (bonne chance)
    </p>
       <h6 class="bottom__line">Règle coma</h6>
       <p>4.1 Lorsque vous tombez dans le coma vous n’oubliez pas la scène entièrement, selon la cause du coma vous pouvez souffrir de certains "trous" de mémoire mais partiels. Jouez en conséquence.
    </p>
       <p>4.2 Interdiction de parler lucidement dans le coma, privilégiez les gémissements, “j’ai mal, je vais mourir, ma jambe, etc...”.
    </p>
       <p>4.3 Lorsque l'item de l'oubli a été utiliser, vous n'avez plus de souvenirs et vous ne pouvez parler.
    </p>
       <h6 class="bottom__line">Règlement illégal</h6>
       <p>5.1 En choisissant un RP criminel/gang/mafia vous êtes plus facilement sujet à la mort RP.
    </p>
       <p>5.2 Toute action commencée avec la police doit se terminer. (On se déconnecte pas dans un coin pour fuir)
    </p>
       <p>5.3 Fonctionnement des groupes:
       <br> ● Groupe officiel = ils font partie du background du serveur ce qui requiert une conduite irréprochable. Ils auront un QG + Business et seront limités à 25 membres et 25 sur scène.
       <br> ● Groupe aspirant = ils font partie du background du serveur qui veut dire une conduite irréprochable. Ils auront un QG et seront limités à 25 membres et 25 sur scène.
       <br> ● Groupe reconnu = le staff connaît leur existence et a validé leur création. Ils seront limités à 25 membres et 25 sur scène.</p>
       <p>5.4 Vous pouvez perdre votre degré de groupe à tout moment si vous faites n’importe quoi.
    </p>
       <p>5.5 Chaque chef de groupe sera le référent aux yeux du staff.
    </p>
       <p>5.6 Les morts RP seront à faire valider auprès du staff.
    </p>
       <p>5.7 Chaque groupe créé doit faire un dossier auprès du staff car les places seront limitées, rapprochez vous d'un staff qui vous expliquera les étapes à suivre.
    </p>
       <p>5.8 Pas de scène de viol, d’humiliation trop profonde ou de harcèlement trop prolongée. Modérez vos propos, cela doit rester Rp une vraie personne est en face de vous.
    </p>
       <p>5.9 Vous pouvez essayer de prendre en sandwich un convoi de police pour récupérer un détenu.
    </p>
       <p>5.1.1 Ne jamais aller dans une zone chaude si vous ne voyez personne pour vous baladez ou bien récupérer un bien.
    </p>
       <p>5.1.2 Il est interdit de braquer le LSPD,GDC,GOUV dans le but de récupérer leurs armes.
    </p>
       <p>5.1.3 Interdiction de rechercher des points de drogue.
    </p>
       <p>5.1.4 Les alliances sont autorisées dans le but du business et non pour agir contre un groupe.
    </p>
       <h6 class="bottom__line">Règlement LSPD</h6>
       <p>6.1 Ne jamais sortir une arme létale ou tirer si le suspect en face n'en a pas une, vous avez un taser pour le maîtriser.
    </p>
       <p>6.2 Les insultes venantes d’un agent ne sont pas très bien vues, à faire avec modération.
    </p>
       <p>6.3 Il est passible d'un ban de donner, abuser de ses armes de service.
    </p>
       <p>6.4 Le ripou reste autorisé avec une approbation du staff et extrêmement limité.
    </p>
       <p>6.5 Il est interdit de vouloir fouiller des personnes n’ayant commis aucune infraction douteuse sauf durant un évènement.
    </p>
       <p>6.6 Vous ne pouvez pas prendre d’informations venant d’un civil sauf si celui-ci signe un dossier d’indic.
    </p>
       <p>6.7 Une fouille de (véhicule,entreprise, appartement, maison) doit être réalisée en présence du propriétaire et d'une raison valable.
    </p>
       <p>6.8 Le LSPD ainsi que le Gouvernement sont des factions extrêmement importantes sur le serveur, vous devez avoir une conduite irréprochable.
    </p>
       <h6 class="bottom__line">Règlement LSDH</h6>
       <p>7.1 Ne pas abuser de ses pouvoir magiques !!
    </p>
       <p>7.2 En intervention; n’abusez pas en prenant des trottoirs à toute vitesse etc.
    </p>
       <p>7.3 Le ripou est autorisé avec validation du staff.
    </p>
       <p>7.4 Essayez de pousser au maximum votre métier, documentez vous et utilisez des bruitages quand il est nécessaire pour une immersion totale.
    </p>
       <p>7.5 Si vous estimez que la personne a fait un accident ou bien une chute très grave et que celui-ci doit avoir une mort RP contactez le staff.
    </p>
       <p>7.6 Si un joueur devient abusif en demandant des soins trop souvent dans la même journée prévenez le staff.
    </p>
       <p>7.7 Ne jamais refuser un soin, sauf cas particulier !
    </p>

